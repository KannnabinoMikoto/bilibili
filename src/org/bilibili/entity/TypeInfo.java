package org.bilibili.entity;

public class TypeInfo {
	private int VideoTypeNo;//类型编号
	private String VideoTypeName;//类型名称
	public int getVideoTypeNo() {
		return VideoTypeNo;
	}
	public void setVideoTypeNo(int videoTypeNo) {
		VideoTypeNo = videoTypeNo;
	}
	public String getVideoTypeName() {
		return VideoTypeName;
	}
	public void setVideoTypeName(String videoTypeName) {
		VideoTypeName = videoTypeName;
	}

	
}
